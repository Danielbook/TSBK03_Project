// Based on something that I think was close to the original data.
// I made modifications to the teacup, the missing bottom of both cup and plate
// plus reorganised so you can easily get the cup and plate as separate objects.

// About modification of classics like these: This is a balance between nostalgia,
// reference value and usefulness. I lean toward the latter, while still keeping
// changes to a minimum so the other issues are not ignored. I do not want these NOT
// to be the Utah dataset, just a refinement. The data I based this on was not pure
// original data anyway (no mirroring, bottom of teapot) so I made  minimal
// modifications to the teacup to make that useful (adding bottom and top
// of center for both plus an offset of the bottom to give it some thickness).
// I also changed the order of the teacup patches in order to 1) make it easy to
// separate the cup and the plate and 2) to make it possible to skip the extra patches
// in case you want the "pure" model. (The tea spoon, however, needed no modifications.)

